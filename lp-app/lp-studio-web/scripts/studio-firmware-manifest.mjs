#!/usr/bin/env node
import fs from "node:fs";

const [manifestFile] = process.argv.slice(2);
if (!manifestFile) {
  throw new Error("usage: studio-firmware-manifest.mjs <manifest-file>");
}

const env = process.env;
const required = [
  "MANIFEST_FIRMWARE_ID",
  "MANIFEST_DISPLAY_NAME",
  "MANIFEST_TARGET",
  "MANIFEST_PROFILE",
  "MANIFEST_SOURCE_COMMIT",
  "MANIFEST_SOURCE_DIRTY",
  "MANIFEST_GENERATED_AT",
  "MANIFEST_IMAGE_PATH",
  "MANIFEST_IMAGE_SIZE",
  "MANIFEST_IMAGE_SHA256",
];

for (const key of required) {
  if (!env[key]) {
    throw new Error(`missing environment variable: ${key}`);
  }
}

const manifest = {
  schemaVersion: 1,
  firmwareId: env.MANIFEST_FIRMWARE_ID,
  displayName: env.MANIFEST_DISPLAY_NAME,
  target: {
    family: "esp32",
    chip: "esp32c6",
  },
  build: {
    package: "fw-esp32",
    target: env.MANIFEST_TARGET,
    profile: env.MANIFEST_PROFILE,
    features: ["esp32c6", "server"],
    sourceCommit: env.MANIFEST_SOURCE_COMMIT,
    sourceDirty: env.MANIFEST_SOURCE_DIRTY === "true",
    generatedAt: env.MANIFEST_GENERATED_AT,
  },
  flash: {
    format: "espflash-merged-image",
    address: "0x0",
    erasePolicy: "write-bootloader-partition-table-and-app",
    mayAffectDeviceData: true,
    resetAfterFlash: true,
    notes:
      "Merged image generated by espflash save-image --merge --skip-padding. Treat as destructive until browser flashing behavior is validated on hardware.",
  },
  images: [
    {
      path: env.MANIFEST_IMAGE_PATH,
      address: "0x0",
      sizeBytes: Number(env.MANIFEST_IMAGE_SIZE),
      sha256: env.MANIFEST_IMAGE_SHA256,
    },
  ],
};

if (!Number.isSafeInteger(manifest.images[0].sizeBytes)) {
  throw new Error(`invalid image size: ${env.MANIFEST_IMAGE_SIZE}`);
}

fs.writeFileSync(manifestFile, `${JSON.stringify(manifest, null, 2)}\n`);
JSON.parse(fs.readFileSync(manifestFile, "utf8"));
